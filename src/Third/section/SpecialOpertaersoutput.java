package Third.section;

public class SpecialOpertaersoutput {

	public static void main(String[] args) {
		doublecodes();
		 SplitOperation();
		 tabOperation();
		 slashOperation();
}

	public static void doublecodes() {
		System.out.println("Hello \" world \" ");
		System.out.println("Hello \" world ");
		System.out.println("\"Hello\" \" world \"");

	}
	
	
	public static void SplitOperation() {
		System.out.println("*Sharath \n Laasya*");
		System.out.println("*Sharath \nLaasya*");
		
	}
	public static void tabOperation() {
		System.out.println("*Sharath \t Laasya*");
		System.out.println("*Sharath \tLaasya*");
		
	}
	public static void slashOperation() {
		System.out.println("*Sharath \\ Laasya*");
		System.out.println("*Sharath \\\\ Laasya*");
		
	}
}
