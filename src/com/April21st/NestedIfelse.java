package com.April21st;

public class NestedIfelse {

	public static void main(String[] args) {
		int a=14;
		if(a==12)
		{
			System.out.println("a = 12");
		}
		else if(a==13){
			System.out.println("a = 13");
		}
		else if(a==14){
			System.out.println("a = 14");
		}
		else {
			System.out.println("a ! = 12 and a != 13 and a!=14");
	}
	}

}
